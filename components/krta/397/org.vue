		<p>방사선기술과학 발전을 위한 양해각서 체결</p>
		<hr>
		<p>대한방사선과학회(회장 김성철)는 ‘대한방사선사협회 경기도회’와 지난 2023년 10월 28일 양해각서를 체결하였다. 본 양해각서의 주된 내용은 ‘대한방사선사협회 경기도회’ 회원이 방사선기술과학 논문지에 제출하여 게재 확정 시 ‘대한방사선사협회 경기도회’에서 회원들의 학술활동 증진을 위하여 게재료를 지원하며(단 논문의 사사에는 반드시 ‘이 논문은 대한방사선사협회 경기도회의 학술활동 지원에 의한 논문임(해당년도).’이라는 문구를 명시해야 함), ‘대한방사선과학회’에서는 게재된 논문에 대하여 사사료 없이 출판료를 산정한다. 대한방사선사협회 경기도회 회원의 게재료 지원에 관한 사항은 경기도회 홈페이지를 통하여 자세히 알 수 있다.</p>
