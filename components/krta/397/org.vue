		<p><span>대한의료기사단체총연합회 제11차 정기 회의</span></p>
		<p>대한의료기사단체총연합회(총회장 황윤숙, 이하 의기총)는 지난 2023년 11월 15일(수) 대한안경사협회(회장 김종석)회관에서 제11차 정기 회의를 개최하였다. </p>
		<p>이날 논의는 2024년 11월 1일부터 시행되는 학생 현장실습 필수 이수를 주 내용으로 하는 「의료기사 등에 관한 법률」 일부 개정의 후속 절차와 면허관리에 대한 보건복지부와 협조 사항 및 각 단체들의 현안 문제에 대한 논의가 있었다.</p>
		<hr>
		<p>제6회 보건의료기사의 날 기념식 성료</p>
		<hr>
		<p><span>‘46만 보건의료기사의 국민 건강을 위한 헌신에 감사’</span></p>
		<p>국민 건강과 보건복지 향상을 위한 46만 보건의료기사의 공로를 되새기고 미래 비전을 모색하기 위해 지난 2018년 제정된 ‘보건의료기사의 날’이 올해로 여섯 돌을 맞았다. 대한의료기사단체총연합회(총회장 황윤숙, 이하 의기총)가 2023년 11월 15일(수) 대한안경사협회 교육센터에서 ‘제6회 보건의료기사의 날 기념식’을 개최했다.</p>
		<p>기념식에는 의기총 황윤숙 총회장을 비롯한 의기총 구성 8개 단체장과 보건복지부 오상윤 의료자원정책과장, 전재진 국회의장 정무특보, 각 단체 관계자 등 100여 명이 모여 자리를 빛냈다. 이날 제6회 보건의료기사의 날 기념식은 황윤숙 총회장의 기념사와 내외빈 축사, 의기총 및 단체 활동보고, 유공자 포상 등의 순서로 진행됐다. 이어 “이러한 국민과의 신뢰, 약속을 지키고, 보건의료환경 변화에 따른 질 높은 보건의료 서비스를 제공하기 위해서는 법적, 제도적 뒷받침이 필수적이다. 의기총에서는 이를 기반으로 올해 학생들의 현장실습 이수 의무화를 중심으로 한 「의료기사 등에 관한 법률」 개정안 통과를 끌어내는 데 주력했다. 복지부를 비롯한 유관 부처와 각 직역 단체가 한마음으로 노력한 덕분에 이룰 수 있었던 성과였다. 앞으로도 전국의 보건의료기사가 국민의 건강을 위한 소임을 다할 수 있도록 의기총에서는 든든한 지원자가 될 것이며, 꾸준히 권익 보호와 제도 개선을 위해 노력하겠다”라고 밝혔다.</p>
		<p>현장을 찾은 오상윤 의료자원정책과장은 “국민을 위한 보건의료기사의 노고, 이러한 회원들의 권익 보호를 위한 의기총의 노력을 잘 알고 있다. 올해 의료기사법 개정 과정에서 의기총과 많은 소통이 있었고, 이후 하위 법령과 규정 마련 과정에서도 간담회를 통해 의견을 나누겠다. 보건의료기사의 구심점으로서 의기총이 앞으로도 많은 역할을 해주길 바란다”라고 축사를 건넸다.</p>
		<p>전재진 국회의장 정무특보 또한 “2018년 첫 보건의료기사의 날 기념식부터 함께하고 있어 의미가 더욱 특별하게 느껴진다. 의기총 8개 단체의 성과를 보면서 소통과 이를 바탕으로 한 단합의 중요성을 다시금 알 수 있었다. 의료기사 관련 제도, 정책 마련과 권익 향상이 국민 건강으로 이어질 수 있도록 협력하겠다”라고 밝혔다. 이날 기념식에는 함께하지 못했지만, 신동근 국회 보건복지위원장과 고영인·강기윤 간사도 영상으로 축하를 전하기도 했다.</p>
		<p>이어 의기총과 소속 단체의 주요 활동사항을 정리한 영상을 기반으로 2023년 활동보고가 진행됐다. 활동보고에 이어서는 보건의료기사의 날을 기념하고, 국민 건강과 보건복지 향상에 기여한 유공자에 대한 포상이 이어졌다. 포상은 보건복지부 장관상과 국회 보건복지위원장상으로 나뉘어, 직역별로 각 1명씩 수여됐다(포상 명단 참조).</p>
		<p>제6회 보건의료기사의 날 기념식을 마친 의기총 황윤숙 총회장은 “올해 지방 권역을 중심으로 의료기사연합회가 새로 발족한 곳도 있고, 공동 행사를 추진한 곳도 있었다. 앞으로도 의기총이 46만 보건의료기사 여러분의 구심점으로서 역할을 할 수 있도록 나아가겠다”라고 전했다.</p>
		<p>한편 의기총은 보건의료분야 의료기사 8개 단체가 소속된 단체로 최적화된 보건 의료분야 서비스 제공을 위하여 전문성 강화와 발전, 국민 건강권 향상을 위해 결성되었으며 대한물리치료사협회, 대한방사선사협회, 대한보건의료정보관리사협회, 대한안경사협회, 대한임상병리사협회, 대한작업치료사협회, 대한치과기공사협회 대한치과위생사협회(가나다순) 총 46만여 회원이 가입된 보건의료분야 단체이다. 의기총 총회장은 대한치과위생사협회장인 황윤숙 회장이 맡고 있다.</p>
