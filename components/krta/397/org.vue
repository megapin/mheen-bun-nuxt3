		<p> “대한의료기사단체총연합회 신임 총회장 선출”</p>
		<hr>
		<p>대한방사선사협회 한정환 회장이 대한의료기사단체총연합회(이하 의기총) 제12차 정기회의에서 2024년 1월 1일부터 1년간 의기총을 이끄는 신임 총회장으로 선출되었다.</p>
		<p>지난 1월 1일부터 공식 임기를 시작한 한정환 총회장은 45만 의료기사를 대표함과 동시에 의기총에서 추진 중인 ‘학생 현장실습 의무화’를 주요 내용으로 한 「의료기사 등에 관한 법률」 개정안과 ‘의료기사 면허신고제 활성화를 위한 방안’ 등 의료기사의 권익 보호와 위상 강화를 위한 활동에 나설 예정이다.</p>
		<p>한정환 회장은 “‘멀리 가려면 함께 가라’는 아프리카 속담처럼 보건의료인의 전문 영역 확립을 위해 함께 연대하여 각 직무 영역에 대한 미래와 비전을 만드는 데 주력하겠다”는 의지를 다졌다.</p>
		<p>대한의료기사단체총연합회는 대한물리치료사협회, 대한방사선사협회, 대한보건의료정보관리사협회, 대한안경사협회, 대한임상병리사협회, 대한작업치료사협회, 대한치과기공사협회, 대한치과위생사협회(가나다순) 등 보건의료기사 8개 단체로 구성되어, 보건의료기사의 공동 발전을 위해 활동하고 있다.</p>
