		<p>협약식에는 대한방사선사협회 한정환 회장, 서영배 부회장, 이미경 연수원장, 임청환 충남도회장, 김기랑 충남도전임회장, 백석문화대 송기신 총장, 이동수 교목실장, 강기정 평생교육원장 등이 참석했다.</p>
