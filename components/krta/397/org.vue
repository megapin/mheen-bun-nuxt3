		<p>또한, 이날 참석한 내·외빈으로는 대한방사선사협회 한정환 회장을 비롯하여 이배원, 서영배, 최정욱 부회장과 대한방사선사협회 김 기랑 부의장, 서울특별시회 박성모 회장, 인천광역시회 방종창 회장, 강원도회 이홍규 회장, 대구광역시회 김청모 회장, 전라북도회 유진오 회장, 부산광역시회 윤주호 회장, 경상남도회 정봉재 회장과 충청 방사선사 연합학술대회의 이익표 준비위원장, 대한방사선치료학회 백금문 회장, 대한방사선사협회 충청남도회 임 청환 회장, 충청북도회 정원희 회장, 대전광역시회 박희왕 회장, 대전광역시회 윤석한 전임 회장을 비롯하여, 대한간호조무사협회 대전/충남 김진석 회장 등이 참석해주어, 내외빈분들의 참여와 회원들의 관심과 재학생들의 동참으로 성황리에 마칠 수 있었다. 폐회식으로 대전광역시 박회왕 회장의 학술대회에 참여해주신 여러 회원분들과 현장에서 공헌해주신 임원진분들께 감사드리는 인사말로 추후 예정되어있는 12월 2일 보수교육에도 많은 성원을 부탁드리는 맺음말을 끝으로 「2023년도 제13차 충청방사선사연합 학술대회」를 성황리에 마무리하였다.</p>
