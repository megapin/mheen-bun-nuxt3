		<p>대한자기공명기술학회(회장 김준규)는 2023년 11월 18일 서울 송파구에 위치한 올림픽파크텔에서 2023년 임원 워크숍을 개최하였다. 11대 임원진이 맞이하는 2번째 워크숍으로 다가오는 겨울의 한기에도 불구하고 많은 임원진들이 참석한 가운데 열띤 토론의 장이 마련되었다.</p>
		<p>김준규 회장은 인사말에서 11대 임원진들이 올 한 해 동안 계획하고 진행했던 모든 행사와 사업들이 아무 사고 없이 진행되어 임원진들의 노고에 감사드린다는 말을 전하였다. 덧붙여 행사 준비와 이뤄낸 사업들에 문제점은 없는지 되짚어 보고 보완해야 할 사항들은 점검하여 다가오는 2024년에는 학회가 더욱 발전하는 방향으로 나아갈 수 있도록 최선을 다해달라는 독려의 말로 워크숍의 시작을 열었다.</p>
		<p>워크숍의 진행은 강충환 총무이사의 회무보고를 필두로 부서별 업무보고 및 2024년 사업추진계획에 대한 발표가 이어졌다.</p>
		<p>중점내용으로는 교육1부에서 올해 각 3월과 6월에 진행하였던 MRI 입문강좌와 임상 기초강좌를 많은 회원들에게 효율적이고, 전문적인 내용을 전달하기 위해 내년에는 MRI 입문강좌와 임상기초강좌를 2일에 걸쳐 양일간 진행하는 계획을 발표하였고, 정보부에서는 리뉴얼된 홈페이지의 회원관리와 홍보내용, 개선되어야할 사항에 대하여 논의 하였다. 교육 2부에서는 올해 개설한 유투브 채널에 대해서 업데이트 되었던 영상 콘텐츠 중 부족했던 점과 준비한 임원들에 대한 감사의 인사와 더불어 앞으로 준비할 교육영상들에 대해 논의하였고, 그 외에 편집부, 섭외부, 국제부 등 다른 부서들에 대한 올 한 해 진행했던 사업 보고가 있었고, 2024년 계획된 사업들에 대한 심도 있는 토론이 이루어졌다.</p>
		<p>김준규 회장과 양선욱, 박용훈 부회장은 각 부서별 발표가 끝날 때 마다 준비한 내용들을 면밀히 확인하고 보완해야 할 점과 앞으로 나아가야할 방향을 제시하며 내년에는 올해보다 더 발전된 학회의 모습이 기대된다는 말을 전했다.</p>
		<p>특히 이번 워크숍은 올 한해를 정리하는 송년회를 겸하는 행사여서 최선을 다해준 임원들과 그 동안 못 다한 이야기와 정을 나누기 위한 자리가 마련되어 더욱 뜻 깊고 의미 있는 행사가 되었다.</p>
