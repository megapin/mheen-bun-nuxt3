		<p>방사선기술과학 논문지 한국연구재단 재인증 평가 결과 등재지 유지</p>
		<hr>
		<p>대한방사선과학회에서 발간하는 ‘방사선기술과학’ 논문지는 금번 한국연구재단 재인증 평가 결과 95.8점의 우수한 점수로 6년간(2023~2028년) 등재지로 유지되었다. 방사선기술과학 논문지는 학술적 가치와 성과가 설립 취지에 맞게 영상의학회지 등 의약학 11개 분야에서 높은 인용지수를 유지하고 있으며, 과학인용색인 확장판(Science Citation Index Expanded; SCIE) 등재를 위해 준비하고 있다. 또한, 본 논문지는 1978년 12월 13일부터 2022년 12월 31일까지 총 1,465편(원저 1,277편, 종설 63편, 강좌 54편, 자료문 30편, 보고문 24편, 특집문 17편)을 발간하였으며, 지난 45년간 꾸준히 발전해온 결과 한국연구재단으로부터 2007년에 등재(후보)학술지, 2015년에 등재학술지로 승인을 획득하였다. 방사선 관련 다양한 주제의 연구논문들이 투고되고 있으며, 회원들의 논문 투고가 증가함에 따라 2006년도부터 연 4회 발행하였으며, 2018년부터는 연 6회(2월, 4월, 6월, 8월, 10월, 12월) 학술지를 발행하고 있다.</p>
