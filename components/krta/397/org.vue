		<p>올바른 보건의료정책 기반 마련을 위한 400만 보건복지의료연대(이하 보건복지연대)는 1일 오전 11시 대한의사협회 대강당에서‘올바른 보건의료정책 기반 마련을 위한 400만 보건복지의료연대 공동 공약 기자회견’을 개최했다.</p>
		<p>보건복지연대는 제22대 국회의원 총선거를 앞두고 대한민국 보건의료 및 복지 분야 전문가들이 금번 총선에 적극적으로 참여할 것을 유도하는 한편, 보건의료 및 복지 분야에 대한 합리적인 제도 개선안 및 올바른 정책대안을 적극적으로 제시해 전문가 단체 연합으로서의 그 역할과 책임을 다할 것임을 분명히 밝혔다. 특히, ‘합리적인 의료 거버넌스 구축과 통합의료돌봄체계 구축, 초고령 사회 대비 실효적 대안마련’이라는 400만 보건복지의료연대의 공동의 목표를 달성하기 위해 다음과 같은 정책을 제안했다.</p>
		<p><span>첫째, 보건의료 및 복지정책 수립시 전문가들의 의견을 존중하여 사회적 책무를 강화하라</span></p>
		<p>합리적인 보건의료 및 복지정책 수립을 위해서는 소수일지라도 관련 전문가의 의견을 적극 반영해야 한다. 보건의료 및 복지정책을 수립함에 있어 실효성과 편의성을 모두 고려해야 하지만 결정 방식은 전문가들의 영역과 비전문가들의 영역으로 반드시 구분해야 합리적인 정책을 수립할 수 있다. 보건의료 및 복지 전문가들이 그 사회적 책무를 다할 수 있도록 보건복지의료 정책 전문가 거버넌스 제도화 등 현실적인 환경 마련이 반드시 이루어져야 한다.</p>
		<p><span>둘째, 보건의료 직역별 업무 안정성을 법률로 보장하라</span></p>
		<p>보건의료 직역별 업무 범위를 명확히 하고 그 내용을 법률로 정해 보건의료 전문가들의 지위를 보장해야 한다. 특히, 혈액관리 실무에 임상병리사를, 산업안전보건법상 보건관리자에 응급구조사를 추가해야 한다. 이와 더불어, 안정적인 진료환경 구축 및 합리적인 의료분쟁 조정 제도 마련을 위한 의료분쟁특례법이 조속히 제정되어야 한다.</p>
		<p><span>셋째, 사회적 변화와 그 요구에 맞는 직역별 역할을 정립하라</span></p>
		<p>‘질병의 치료’뿐만 아니라 ‘예방과 관리’의 개념으로까지 의료의 패러다임이 확대되고 있다. 이런 사회적 변화에 발맞춰 보건의료서비스를 진료실 내로 한정짓지 말고 지역사회 곳곳으로 그 역할과 참여를 확대해 나가야 한다. 이러한 사회적 변화와 요구에 맞는 각 직역별 역할 정립을 위한 합리적인 논의 진행을 위해 ‘보건의료인력 업무조정위원회’를 구성·운영해야 한다.</p>
		<p>넷째, 직역별 역량 강화를 위한 전문성 제고 및 처우개선을 위한 실질적인 정책을 실시하라</p>
		<p>의료기사의 역량 강화를 위해 의료기사 교육 평가원을 설립하는 한편, 의료기사 등의 영역별 자격 제도를 도입해 체계적인 시스템 마련을 도모해야 한다. 간호조무사의 학력을 고졸로 제한하고 있는 불합리한 규제를 철폐하고, 시대의 요구에 맞는 역량을 보다 체계적으로 쌓아갈 수 있도록 관계 법령 개정 및 교육시스템 마련이 시급하다. 아울러, 요양보호사의 처우 및 근무환경 개선을 위한 현실적인 대책을 마련해야 한다.</p>
		<p><span>다섯째, 대체의료인력 지원과 면허 및 자격의 신고와 관리의 효율성을 위한 보건의료인력 관리시스템을 확립하라</span></p>
		<p>효율적인 통합의료 돌봄시스템 구축과 운영을 위해 보건의료 인력의 적재적소 배치가 가장 중요하다. 이를 위해, 현행 보건의료인력 관리시스템을 개선해 효과적인 관리시스템으로 전환하고 높아지는 국민적 요구에 부응하는 체계적인 시스템으로 발전시켜야 한다.</p>
		<p><span>여섯째, 일차의료 중심의 통합의료 돌봄 서비스를 확립하라</span></p>
		<p>2025년 초고령사회 진입을 목전에 두고 있는 현시점에서, 고령인구, 만성질환자 등에 대한 보다 체계적이고 적극적인 통합의료 돌봄 시스템 마련이 필요하다. 특히, 지역별 커뮤니티의 특수성이 반영된 복합적인 서비스 제공을 위해 일차의료기관 의사의 지도 아래 ‘방문간호, 방문재활’ 등이 체계적으로 진행되어야 함은 물론이고, ‘고령인구, 만성질환자, 장애인, 퇴원환자’ 등을 위한 보다 적극적이고 통합된 재택의료서비스로의 ‘적극적 돌봄 서비스’가 이루어 져야 한다. 이러한 체계적인 시스템 마련을 위해, ‘재택의료센터, 의원부설 방문간호·재활센터’등 운영이 제도화 되어야 한다.</p>
		<p><span>일곱째, 고령인구의 의료 접근성을 보장하기 위한 제도 개선을 추진하라</span></p>
		<p>노인 환자의 의원급 의료기관 이용 부담을 낮추기 위하여 노인외래정액제도의 현실적인 개선이 필요하다. 특히, 구간 별 노인 환자의 본인부담 비율이 현실과 맞지 않아 노인환자 개인의 부담이 급증하고 있어 문제가 되고 있다. 각 구간별 기준을 합리적으로 조정해 노인 환자가 적절한 의료서비스를 받을 수 있도록 해야 한다. 아울러, 현행 65세 이상 인구의 임플란트 보장을 ‘2개’에서 ‘4개’로 확대해 적절한 치과 진료 서비스를 보다 적시 받을 수 있도록 시급히 제도를 개선해야 한다. </p>
		<p>보건복지연대는 이상과 같은 합리적인 정책을 제시하고, 향후 각 후보자의 공약을 확인·체크해 합법적인 절차와 방식을 통해 올바른 정책을 반영하는 정당과 후보자를 발굴·지지할 것임을 밝혔다.</p>
		<p>대한방사선사협회에서는 한정환 회장(대한의료기사단체총연합회 총회장), 이배원 부회장, 최정욱 부회장, 지은 부장, 김대한 부장, 사무국이 참석했다.</p>
