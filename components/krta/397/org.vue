		<p>대한인터벤션영상기술학회, 보험연수교육 개최</p>
		<hr>
		<p>대한인터벤션영상기술학회(학회장 정희동)는 2023년 11월 18일 토요일 대구광역시 계명대학교 동산병원에서 보험연수교육(보수교육 4평점)을 개최하였다. 대구에서 보기 힘든 눈보라와 영하의 추위 속에서도 150명의 방사선사 회원과 50여 명의 중재시술 및 보험심사 간호사가 참석하여 뜨거운 관심을 보여주었다.</p>
		<p>2023년 보험연수교육은 4교시 강의로 진행됐다. </p>
		<p><span><img src="/imgs/org/179.png" /> </span><span>1교시 뇌신경 중재시술의 보험, 이재일 교수<br /></span>_대한신경중재치료의학회(부산대학교병원)</p>
		<p><span><img src="/imgs/org/179.png" /> </span><span>2교시 몸통과 팔다리 중재시술의 보험, 권세환 교수<br /></span>_대한인터벤션영상의학회(경희대학교병원)</p>
		<p><span><img src="/imgs/org/179.png" /> </span><span>3교시 심장 중재시술의 보험, 박경진<br /></span>_대한심혈관기술연구회(국민건강보험 일산병원)</p>
		<p><span><img src="/imgs/org/179.png" /> </span><span>4교시 중재시술의 사례별 질문과 답변, 안주용<br /></span>_학회 보험이사(가톨릭대학교 서울성모병원)</p>
		<p>2교시 후에는 원내 식당에서 점심식사를 제공하여 회원들의 참여와 만족도를 높이고, 회원들끼리 서로 정보를 교류할 수 있는 자연스러운 기회를 제공하였다. 보험연수교육은 매년 가을에 열리며 대한인터벤션영상기술학회에서 주관하는 행사로 대한인터벤션영상의학회와 대한신경중재치료의학회의 후원을 받고 국민건강보험 심사평가원의 협조를 얻어 개최한다. 각 병원별, 시술의별로 중재시술의 특성과 성격이 다르고, 심사평가의 세밀한 기준에 있어서 지역에 따라 차이를 보이기 때문에 회원들은 중재시술 업무에서 시술료와 재료대의 급여 기준에 대한 선택과 결정에 망설여지게 된다. 특히 매년 개정되는 보건복지부 고시(2023년 개정된 중재시술 고시 건수: 14건)와 새롭게 개발되는 중재시술 재료 등은 그 경험이 없을 때 스트레스가 가중된다. 이에 보험연수교육은 회원들의 이 같은 어려움을 해결하기 위하여, 중재시술과관련된 신설 및 개정 고시를 공유하고 다양한 경험을 보유한 강사진을 섭외하여 새롭게 중재시술을 접하는 회원들이 다가가기 쉽도록 요양급여비용의 심사 지침부터 실제 심사 사례까지 강의에 포함하여 그 이해를 돕고 있다.</p>
		<p>특히 이번 보험연수교육에서는 1, 2교시 강의에서 의학회의 활발한 연구와 심사평가원 자문활동으로 유명한 중재시술 전문의 두 분을 초빙하여 행사의 질적 상승을 꾀하였고, 최초로 심장중재시술 관련 강의(3교시)를 개설하여 머리/몸통과 함께 심장시술을 동시에 치료하는 지역 병원의 회원들의 만족도를 높이고자 하였다. 또한 4교시에서는 지난 2년 동안 학회 보험게시판을 통하여 게시된 약 80건의 질문 중에서 가장 화제가 되었던 사례를 선별하여 관련 고시를 함께 제시하고 토론을 통하여 적절한 해결책을 모색하는 방식으로 강의가 진행되었다. 토론을 주도한 안주용 보험이사와 과거 학회 보험이사를 역임했던 전문가들이 패널로 자리하여 회원들의 궁금증을 해소하는 데 많은 도움이 되었다.</p>
		<p>역사와 전통을 자랑하는 보험연수교육은 올해 여러 가지 새로운 시도를 통해 한 층 더 발전하였다. 연수교육 후에 회원들을 대상으로 모바일 설문을 시행한 결과 72.5%에서 긍정적인 평가를 받았고 13%에서는 개선의 요구가 있었다. 첫술에 배부를 수 없고 모두를 만족시키는 것은 불가능하지만, 발전을 위해 용기를 가지고 새로운 시도를 하였다는 것이 이번 보험연수교육의 가장 큰 성과가 아닐까 싶다. </p>
		<p>정희동 대한인터벤션영상기술학회장은 “이번 보험연수교육을 통해서 인터벤션 전문 방사선사로서의 자질과 능력을 향상하게 하고 적절한 급여기준 및 시술재료를 사용하므로써 국민에 대한 신뢰를 향상하고 변화하는 의료환경에 신속하고 적절한 대처를 할 수 있겠다.”는 인사말로 행사를 마무리하였다. 끝으로 행사가 원활하게 진행될 수 있도록 준비해주신 민순기 부회장(대구가톨릭대학교병원)과 동산병원 관계자 여러분께 감사의 말씀을 전한다.</p>
