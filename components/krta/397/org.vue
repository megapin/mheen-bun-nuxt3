		<p>논어(論語)의 미자편(微子篇)에는 ‘왕자불간(往者不諫) 내자가추(來者可追)’란 한자성어가 있다. 이는 ‘이미 지나간 일은 어찌할 도리가 없으며 미래의 일을 경계해야 전과 같은 과실을 범하지 않는다.’ 라는 뜻이다. 대한 디지털의료영상학회는 다사다난(多事多難)했던 2023년 계묘년 (癸卯年)을 돌아보고 오는 2024년 갑진년 (甲辰年) 에는 ‘내자가추(來者可追)’ 하는 의미로 2023년 12월 15일 금요일 건국대학교병원에서 마지막 정기이사회 및 송년회를 개최하였다.</p>
		<p>이번 정기 이사회는 대한디지털의료영상학회가 2023년 한해를 돌아보고 새롭게 오는 청룡 [靑龍]의 해 인 갑진년 (甲辰年)의 새로운 각오를 다지는 시간 이었다. 이 자리에는 김광수 회장(서울아산병원), 김종성 부회장(서울아산병원), 석종민 부회장(서울경찰병원), 이성주 총무이사(서울대학교병원), 임종천 교육이사(서울한양대학교병원) 정홍문 법제 및 공보이사 (대구보건대학교), 박범진 정통이사(경희대학교병원), 김명성 국립암센터 (편집이사), 홍주완 연구이사(을지대학교), 서정범 관제이사( 건국대학교병원)등 많은 이사 및 부장 들이 참가하여 자리를 빛내 주었다. 김광수 회장(서울아산병원)의 업무와 감사보고를 시작으로 대한디지털영상학회의 2023년 운영 및 전체 업무를 임원들과 같이 함께 되돌아보는 시간이었다. 또한 2023년의 행여 놓진 과오(過誤)를 반면교사(反面敎師) 로 삼기로 하는 결의 시간이기도 하였다. 불가리아의 속담 중 ‘한 방울 한 방울 물이모여 호수가 될 때까지’ 라는 격언이 있다. 이 말은 어떠한 결과를 내기 위해 조금씩 조금씩 위대함에 다가가는 것을 뜻한다. 갑진년(甲辰年) 에는 대한디지털의료영상학회의 모든 임원들의 노력과 헌신의 땀을 한방울 한방울 이라도 조금씩 모아 더욱 더 좋은 학회가 되기를 기원해 본다.</p>
