		<p><span>-α err prob: </span><span>0.05 → 유의수준을 설정</span></p>
		<p><span>-Power: </span><span>0.8 → 보통 0.8로 또는 0.95로도 많이 설정</span></p>
		<p><span>-Allocation ratio N2/N1: </span><span>1 → 두 그룹의 표본수 비를 정하는 것으로, 두 그룹에 동일한 표본수를 넣고 싶으면 1, 한 그룹이 다른 그룹보다 2배 많게 표본수를 넣고 싶으면 2라고 설정(보통은 1)</span></p>
