		<p><span>① Test family: </span><span>F tests</span></p>
		<p><span>② Statistical test: </span><span>ANOVA: Fixed effects, omnibus, one-way</span></p>
		<p><span>③ Type of power analysis: </span><span>A prior: Compute required sample size-given α, power, and effect size</span></p>
		<p><span>④ Input Parameters: </span></p>
		<p><span>-Effect size f: </span><span>0.25 </span></p>
		<p><span>-α err prob: </span><span>0.05</span></p>
