		<p><span>이러한 인공지능 관련 기술이 지속적으로 개발됨에 따라, AI 창작물의 저작권 보호를 위한 목소리 또한 커지고 있다. 한국저작권위원회의 보고서에 따르면, AI로 생성된 작품에 대한 저작권 등록 신청은 거부되었으며, 저작권청은 인간이 만든 작품만 등록할 수 있다는 규정을 갖고 있다. 인공지능은 저작자의 지위나 저작권 향유의 주체가 될 수 없지만, 인간의 창작적 기여를 통해 만들어진 예술작품에 대해서는 인간이 저작자로서 저작권 보호를 받을 수 있다는 것이다. 이러한 이유로 대부분의 국가에서는 인공지능에게 저작권을 부여하는 것이 어려운 실정이다.</span></p>
