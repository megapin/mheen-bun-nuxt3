		<p><span>Chat GPT에 대한 이해 - 교양편</span></p>
		<p><span>인공지능의 세계에 발을 들이며, 우리는 새로운 시대의 문턱에 서 있다. 곳곳에서 인공지능, Chat GPT에 대한 이야기가 쏟아져 나오고 있다. 자칫 진부하게 느껴질 수 있는 소재이지만 우리는 이에 대해 잘 안다고 자신 있게 이야기 할 수 있을까? Chat GPT에 대해 사람들과 얼마나 이야기할 수 있을까? Chat GPT는 우리의 일상 대화에 혁신을 가져올 수 있는 강력한 도구이다. 그러나 이 도구를 효과적으로 활용하기 위해서는 그 기능과 작동 원리에 대한 이해가 필요하다.</span></p>
		<p><span>필자는 책 “챗 GPT 질문의 기술” 읽고 방사협보를 애독하는 선생님들과 함께 앞으로 우리의 삶을 바꾸어 놓을 인공지능 기술, 그리고 그 중심에 있는 Open AI의 대화형 AI 모델인 Chat GPT에 대해 알아보는 시간을 가지려 한다. 학술적으로 깊이 있게 알아가기보다는 전반적인 Chat GPT에 대한 이해를 통해 여러 사람들과 대화를 나눌 수 있는 교양을 쌓는다는 생각으로 편하게 읽어 주시기를 바란다.</span></p>
		<p><span>Chat GPT는 단 두 달 만에 월간 10억 명의 사용자를 유치하며 세계적으로 관심을 받았다. 이는 거대한 움직임이 일어나고 있음을 분명히 보여 준다. 거대 언어 모델의 시작은 2012년 구글의 연구자들에 의해 발표되었으며, 2019년 GPT-1이 업계 최고 성능의 자연어 처리 모델을 뛰어넘는 기록을 세우며 OpenAI가 주목받기 시작했다. 2020년에는 부정확한 정보를 사실처럼 제공하는 문제를 수정하고, 2023년에는 단 3개월 만에 Chat GPT-3(GPT3.5)를 일반 공개하고 GPT-4를 유료 사용자를 대상으로 한정적으로 공개했다. GPT-4에서는 잘못된 정보나 유해정보 생성 가능성이 크게 줄어들었으며, 추론 및 요약 능력과 함께 이미지 해석이 가능한 멀티모달 형태를 가지게 되었다.</span></p>
		<p><span>Chat GPT의 이름에는 의미가 담겨 있다. ‘Chat’은 대화형이라는 의미로서, 대중에게 큰 반향을 일으킬 수 있는 이유 중 하나이다. 이전의 검색 방식에서 질문으로 나아가 요청으로 진화하는 정보 검색 방식은 편의성과 효율성의 진보를 의미하기 때문이다. ‘GPT’는 ‘Generative Pre-trained Transformer’의 약자로, 대규모 텍스트 데이터 세트에서 학습한 내용을 기반으로 통계적 추론을 통해 콘텐츠를 생성한다는 의미이다. 이는 Chat GPT가 챗봇에 국한되지 않고 언어 번역, 문서 요약, 가상 비서 등 다양한 작업에 활용될 수 있음을 나타낸다.</span></p>
		<p><span>미래에는 Chat GPT가 본격적인 AI 플랫폼으로 성장할 수 있는 ‘플러그인 스토어’를 공개하여 전 세계의 개발자들을 불러들이고자 하는 계획을 가지고 있다. 이러한 변화로 인해 Chat GPT에 대한 열광은 불가피한 결과라 할 수 있다. Chat GPT에 대해 열광하는 이유를 정리해 보면 다음과 같다. 첫째로, 기존의 방식이 사용자의 말을 듣고 유사한 정보를 검색하는 것이었다면, Chat GPT는 질문의 의도를 충분히 이해한 뒤 해당하는 정보를 선별하여 답변을 작성해 준다. 둘째로, Chat GPT는 질문의 의도를 이해할 뿐만 아니라 맥락을 유지하며 대화를 계속할 수 있다. 이는 인간의 대화와 유사하게 대명사나 주어, 목적어 등을 생략하여 효율적인 대화를 가능하게 한다. 셋째로, Chat GPT는 단순한 질문뿐만 아니라 이해 분석, 비교, 평가, 풀이, 번역, 해설 등 다양한 요청과 통합된 대화 방식을 가능하게 한다. 이러한 특징들로 인해 Chat GPT는 사람이 하면 2~3일이 걸릴 일을 한 번의 요청으로 처리할 수 있어 뛰어난 효율성을 가지고 있다.</span></p>
		<p><span>Chat GPT와 관련해서 마이크로소프트는 최신 모델인 GPT-4를 마이크로소프트의 검색 엔진인 ‘새로운 Bing’에 적용했다. Chat GPT와 Bing은 모두 트랜스포머 기반 모델을 사용하지만 작동 방식에 약간의 차이가 있다. Chat GPT는 질문에 의도를 이해하고 답변의 창의성과 추론 능력에서 뛰어난 성능을 보이지만 신뢰도 측면에서는 어느 정도 취약성을 보인다. Bing은 검색 페이지의 사용자 편의성을 개선하면서도 답변의 신뢰성을 유지하는 데 초점을 맞추어 차별화된 장점을 가지고 있다.</span></p>
